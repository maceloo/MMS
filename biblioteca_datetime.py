# USANDO BIBLIOTECA DATETIME
import datetime #Importa a biblioteca datetime
print(datetime.date.today())
data_atual = datetime.date.today() #Armazena na variável a data completa
ano_atual = data_atual.year # Armazena na variável o ano atual
print(ano_atual)
mes_atual = data_atual.month # Armazena na variável o mês atual
print(mes_atual)
dia_atual = data_atual.day # Armazena na variável o dia atual
print(dia_atual)
