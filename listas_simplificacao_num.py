# SIMPLIFICAÇÃO DE FORMA-PRIMITIVA 
num = [15,45,30,55,75,65,85,95]
print(f"A soma dos valores é: {sum(num)}") # FUNÇÃO SUM SOMA TODOS OS VETORES 
print(f"O maior valor é: {max(num)}") # FUNÇÃO MAX FORNECE O MAIOR VETOR
print(f"O menor valor é: {min(num)}") # FUNÇÃO MIM FORNECE O MENOR VETOR
print(f"A média dos valores é: {sum(num)/len(num):.2f}") #FORMA DE FAZER MÉDIA USANDO COMANDO LEN 
num_01 = num.sort() # FUNÇÃO PARA COLOCAR O VETOR EM ORDEM CRESCENTE
print(f"A ordem crescente {num_01}")
